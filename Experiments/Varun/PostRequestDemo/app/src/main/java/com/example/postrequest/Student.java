package com.example.postrequest;

public class Student {

    private Long id;

    private String firstName;

    private String lastName;

    private int age;

    private String email;

    public Long getId() {
        return id;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public int getAge() {
        return age;
    }

    public String getEmail() {
        return email;
    }
}
